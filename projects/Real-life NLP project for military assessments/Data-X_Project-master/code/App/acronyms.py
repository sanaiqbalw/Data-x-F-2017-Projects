from nltk import word_tokenize

def ACR_Replace(string, start=0):
    '''This function replaces words in the input string with acronyms from the acronyms dictionary'''

    split_string = word_tokenize(string)

    #Compares the words from the string to the acronym dictionary
    #If one of the words is in the dictionary it replaces it and exits
    output = []
    i = start
    #the outer loop determines the maximum acronym length
    for j in range(6):
        for k in range(start,len(split_string)-j):
            x = ' '.join(split_string[k:k+j+1])
            i+=1
            if x in acronyms:
                new_string = string.replace(x, acronyms[x])
                output = output + ACR_Replace(new_string, i)
        
    output.append(string)
    return output

